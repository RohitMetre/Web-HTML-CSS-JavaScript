//VARIABLE DECLARATION



//VAR,LET AND CONST KEYWORD

//VAR KEYWORD
//Redefining the variable

{
    var a = 10
    console.log("a "+a)  //a=10
     a = 20.5
    console.log("a "+a) //a=20.5
    }
    
    
    
    //Note: Variable can be Redefined if it is created using var keyword


//LET KEYWORD
//Redefining the variable

{
    let b=10
    console.log("b="+b)  //b=10
     b=20.5
    console.log("b="+b) //b=20.5
    }
    
    
    
    //Note: Variable can be Redefined if it is created using let keyword.
    


//CONST KEYWORD
//Redefining the variable

{
    const c=10
    console.log("c="+c) //c=10 
    c=20.5
    console.log("c="+c) //TypeError:Assignment to constant variable
    }
    
    
    
    //Note: Variable cannot be Redefined if it is created using const keyword.
    


    
    
    