//VARIABLE DECLARATION

//IN JAVASCRIPT,VARIABLES MUST BE DECLARED WITH EITHER VAR OR LET OR CONST KEYWORD

//VAR,LET AND CONST KEYWORD

//VAR KEYWORD
//DECLARING THE VARIABLE USING VAR KEYWORD

var a=10
document.write("a="+a)


//LET KEYWORD
//DECLARING THE VARIABLE USING LET KEYWORD

let b=10.5
document.write("b="+b)


//CONST KEYWORD
//DECLARING THE VARIABLE USING CONST KEYWORD

const c=10
document.write("c="+c)





