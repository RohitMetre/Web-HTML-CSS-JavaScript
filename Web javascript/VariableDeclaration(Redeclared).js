//VARIABLE DECLARATION



//VAR,LET AND CONST KEYWORD

//VAR KEYWORD
//Redeclared the variable

{
    var a = 10
    console.log("a "+a)  //a=10
    var a = 20
    console.log("a "+a) //a=20
    }
    
    
    
    //Note: Variable can be Redeclared if it is created using var keyword


//LET KEYWORD
//Redeclared the variable

{
    let a = 10
    console.log("a="+a)  
    let a = 20
    console.log("a="+a) 
    }
    
    
    
    //Note: Variable cannot be Redeclared if it is created using let keyword.
    //syntaxError:Identifier 'a' has already been declared.


